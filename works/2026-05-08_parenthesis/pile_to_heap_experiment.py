def normalize_log(arr):
    return (arr - arr.min()) / (arr.max() - arr.min())

def smooth(arr, w=5):
    return np.convolve(arr, np.ones(w)/w, mode='same')

def make_figure(gs_k, gs_ps, matter_k, matter_pk, out_path):
    amber = '#d48a25'
    blue  = '#5090d8'
    bg    = '#070910'
    panel = '#0c0f18'
    grid  = '#222235'
    text  = '#aaaabc'
    dim   = '#666677'

    fig = plt.figure(figsize=(16, 10), facecolor=bg)
    gs_layout = gridspec.GridSpec(
        2, 3, figure=fig,
        left=0.07, right=0.96, top=0.91, bottom=0.09,
        wspace=0.38, hspace=0.52
    )

    def style_ax(ax, title):
        ax.set_facecolor(panel)
        ax.tick_params(colors=text, labelsize=8)
        ax.xaxis.label.set_color(text)
        ax.yaxis.label.set_color(text)
        ax.set_title(title, color='#ccccdd', fontsize=9.5, pad=8)
        for sp in ax.spines.values():
            sp.set_edgecolor(grid)
        ax.grid(True, which='both', alpha=0.18, color=grid, linewidth=0.6)

    # --- Panel A: Gray-Scott B field (spatial pattern) ---
    axA = fig.add_subplot(gs_layout[0, 0])
    style_ax(axA, 'Gray-Scott B Field\n(quark-like component, stable Turing pattern)')
    # We need the B array — will be passed via closure
    axA._gs_placeholder = True   # filled after simulation

    # --- Panel B: Gray-Scott power spectrum ---
    axB = fig.add_subplot(gs_layout[0, 1])
    style_ax(axB, 'Gray-Scott Power Spectrum\nP(k) vs spatial frequency k')
    gs_ps_s = smooth(gs_ps, 3)
    axB.loglog(gs_k, gs_ps_s, color=amber, linewidth=1.6, alpha=0.92)
    axB.set_xlabel('k  (spatial freq, 2π/px)', fontsize=8)
    axB.set_ylabel('P(k)', fontsize=8)
    # Mark dominant peak
    peak_idx = np.argmax(gs_ps_s)
    axB.axvline(gs_k[peak_idx], color=amber, alpha=0.3, linewidth=1, linestyle='--')
    axB.text(gs_k[peak_idx]*1.15, gs_ps_s[peak_idx]*0.5,
             f'k₀={gs_k[peak_idx]:.3f}', color=amber, fontsize=7, alpha=0.8)
    axB.text(0.05, 0.08, 'Da=1.0 (lepton-like)\nDb=0.5 (quark-like)',
             transform=axB.transAxes, color=dim, fontsize=7, fontstyle='italic')

    # --- Panel C: Matter power spectrum ---
    axC = fig.add_subplot(gs_layout[0, 2])
    style_ax(axC, 'Cosmic Matter Power Spectrum\nCAMB / Planck 2018 best-fit')
    axC.loglog(matter_k, matter_pk, color=blue, linewidth=1.6, alpha=0.92)
    axC.set_xlabel('k  (h Mpc⁻¹)', fontsize=8)
    axC.set_ylabel('P(k)  (Mpc/h)³', fontsize=8)
    # Mark BAO peak region
    eq_idx = np.argmax(matter_pk)
    axC.axvline(matter_k[eq_idx], color=blue, alpha=0.3, linewidth=1, linestyle='--')
    axC.text(matter_k[eq_idx]*1.15, matter_pk[eq_idx]*0.4,
             f'k_eq≈{matter_k[eq_idx]:.3f}', color=blue, fontsize=7, alpha=0.8)
    axC.text(0.05, 0.08,
             'Baryon-photon plasma:\ndifferential diffusion\nin primordial soup',
             transform=axC.transAxes, color=dim, fontsize=7, fontstyle='italic')

    # --- Panel D: Normalized shape overlay ---
    axD = fig.add_subplot(gs_layout[1, :])
    style_ax(axD, 'Shape Comparison — Normalized Log-k  '
             '(testing for shared Turing-class mathematical structure)')

