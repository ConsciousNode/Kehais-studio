# ============================================================
# 1. GRAY-SCOTT SIMULATION
# dA/dt = Da·∇²A − AB² + F(1−A)
# dB/dt = Db·∇²B + AB² − (F+K)B
# ============================================================

def run_gray_scott(N=300, steps=6000, F=0.055, K=0.062,
                   Da=1.0, Db=0.5, seed=42):
    print(f"  Gray-Scott: N={N}, {steps} steps, F={F}, K={K}")
    rng = np.random.default_rng(seed)

    A = np.ones((N, N), dtype=np.float64)
    B = np.zeros((N, N), dtype=np.float64)

    # Sparse random seeds — scattered proto-processes
    mask = rng.random((N, N)) < 0.05
    b_vals = rng.uniform(0.4, 0.9, mask.sum())
    B[mask] = b_vals
    A[mask] = 1.0 - b_vals * 0.7

    # 9-point weighted Laplacian (periodic boundary via 'wrap')
    kernel = np.array([[0.05, 0.20, 0.05],
                       [0.20, -1.0, 0.20],
                       [0.05, 0.20, 0.05]])

    for i in range(steps):
        if i % 2000 == 0:
            print(f"    step {i}/{steps}  (B mean: {B.mean():.4f})")
        lapA = convolve(A, kernel, mode='wrap')
        lapB = convolve(B, kernel, mode='wrap')
        rxn  = A * B * B
        A += Da * lapA - rxn + F * (1 - A)
        B += Db * lapB + rxn - (F + K) * B
        np.clip(A, 0, 1, out=A)
        np.clip(B, 0, 1, out=B)

    print(f"    done. B range: [{B.min():.4f}, {B.max():.4f}]")
    return A, B


def radial_power_spectrum(field):
    """
    Compute radially-averaged 2D power spectrum.
    Returns (k, P(k)) where k is in units of 2π/N per pixel.
    """
    N = field.shape[0]
    f = field - field.mean()
    fft2   = np.fft.fft2(f)
    power  = (np.abs(np.fft.fftshift(fft2)) ** 2) / (N * N)

    cx, cy = N // 2, N // 2
    y, x   = np.ogrid[0:N, 0:N]
    r      = np.sqrt((x - cx)**2 + (y - cy)**2).astype(int)

    max_r = min(cx, cy)
    ps    = np.zeros(max_r)
    for ri in range(max_r):
        m = (r == ri)
        if m.any():
            ps[ri] = power[m].mean()

    k = np.arange(max_r) * (2 * np.pi / N)
    return k[1:], ps[1:]


# ============================================================
# 2. MATTER POWER SPECTRUM (CAMB, Planck 2018 best-fit)
# ============================================================

def get_matter_power_spectrum():
    print("  Matter P(k) via CAMB (Planck 2018)...")
    import camb

    pars = camb.CAMBparams()
    pars.set_cosmology(
        H0=67.36,
        ombh2=0.02237,   # baryon density
        omch2=0.12000,   # cold dark matter density
        mnu=0.06,
        omk=0,
        tau=0.0544
    )
    pars.InitPower.set_params(As=2.1e-9, ns=0.9649)
    pars.set_matter_power(redshifts=[0.], kmax=20.0)
    pars.NonLinear = camb.model.NonLinear_none

    results = camb.get_results(pars)
    kh, _, pk = results.get_matter_power_spectrum(
        minkh=1e-4, maxkh=10.0, npoints=600
    )
    print(f"    done. k range: [{kh.min():.2e}, {kh.max():.2e}] h/Mpc")
    return kh, pk[0]


